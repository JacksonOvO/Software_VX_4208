/******************************************************************
*函数名称:Get_WarnStatus
*功能描述:获取IO 告警信息
*输入参数:无
*输出参数:
*返回值:TRUE  有告警
                    FALSE  无告警
*其它说明:
*修改日期    版本号   修改人    修改内容
*---------------------------------------------------
*2017/12/5                             lwe004 we013
******************************************************************/
BOOL Get_WarnStatus ( void )
{
    uint16_t  usIndex = 0;
    uint8_t   *ucpWarnData = gucaWarnData;
    uint16_t  usCount =  MAX_WARN_DATA_NUM >> BIT_NUM_PER_BYTE_OFF;
    
    for ( ; usIndex < usCount; ++usIndex )
    {
        if ( *ucpWarnData != IO_NO_WARN )
        {
            return TRUE;
        }
        ++ucpWarnData;
    }
    return FALSE;    
}

